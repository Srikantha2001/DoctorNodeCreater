#!/bin/bash

function createDocOrg() {
  infoln "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/docorg.doctor.com/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/docorg.doctor.com/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname ca-docorg --tls.certfiles "${PWD}/organizations/fabric-ca/docorg/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-docorg.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-docorg.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-docorg.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-docorg.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/docorg.doctor.com/msp/config.yaml"

  # Copy docorg's CA cert to docorg's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/docorg.doctor.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/docorg/ca-cert.pem" "${PWD}/organizations/peerOrganizations/docorg.doctor.com/msp/tlscacerts/ca.crt"

  # Copy docorg's CA cert to docorg's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/docorg.doctor.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/docorg/ca-cert.pem" "${PWD}/organizations/peerOrganizations/docorg.doctor.com/tlsca/tlsca.docorg.doctor.com-cert.pem"

  # Copy docorg's CA cert to docorg's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/docorg.doctor.com/ca"
  cp "${PWD}/organizations/fabric-ca/docorg/ca-cert.pem" "${PWD}/organizations/peerOrganizations/docorg.doctor.com/ca/ca.docorg.doctor.com-cert.pem"

  
  infoln "Registering user"
  set -x
  fabric-ca-client register --caname ca-docorg --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/docorg/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering the org admin"
  set -x
  fabric-ca-client register --caname ca-docorg --id.name docorgadmin --id.secret docorgadminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/docorg/ca-cert.pem"
  { set +x; } 2>/dev/null


  infoln "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:7054 --caname ca-docorg -M "${PWD}/organizations/peerOrganizations/docorg.doctor.com/users/User1@docorg.doctor.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/docorg/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/docorg.doctor.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/docorg.doctor.com/users/User1@docorg.doctor.com/msp/config.yaml"

  infoln "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://docorgadmin:docorgadminpw@localhost:7054 --caname ca-docorg -M "${PWD}/organizations/peerOrganizations/docorg.doctor.com/users/Admin@docorg.doctor.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/docorg/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/docorg.doctor.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/docorg.doctor.com/users/Admin@docorg.doctor.com/msp/config.yaml"

}



function createOrderer() {
  infoln "Enrolling the CA admin"
  mkdir -p organizations/ordererOrganizations/doctor.com

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/ordererOrganizations/doctor.com

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:9054 --caname ca-orderer --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-9054-ca-orderer.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-9054-ca-orderer.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-9054-ca-orderer.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-9054-ca-orderer.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/ordererOrganizations/doctor.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy orderer org's CA cert to orderer org's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/ordererOrganizations/doctor.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem" "${PWD}/organizations/ordererOrganizations/doctor.com/msp/tlscacerts/tlsca.doctor.com-cert.pem"

  # Copy orderer org's CA cert to orderer org's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/ordererOrganizations/doctor.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem" "${PWD}/organizations/ordererOrganizations/doctor.com/tlsca/tlsca.doctor.com-cert.pem"

  infoln "Registering orderer"
  set -x
  fabric-ca-client register --caname ca-orderer --id.name orderer --id.secret ordererpw --id.type orderer --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Registering the orderer admin"
  set -x
  fabric-ca-client register --caname ca-orderer --id.name ordererAdmin --id.secret ordererAdminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  infoln "Generating the orderer msp"
  set -x
  fabric-ca-client enroll -u https://orderer:ordererpw@localhost:9054 --caname ca-orderer -M "${PWD}/organizations/ordererOrganizations/doctor.com/orderers/orderer.doctor.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/ordererOrganizations/doctor.com/msp/config.yaml" "${PWD}/organizations/ordererOrganizations/doctor.com/orderers/orderer.doctor.com/msp/config.yaml"

  infoln "Generating the orderer-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://orderer:ordererpw@localhost:9054 --caname ca-orderer -M "${PWD}/organizations/ordererOrganizations/doctor.com/orderers/orderer.doctor.com/tls" --enrollment.profile tls --csr.hosts orderer.doctor.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the orderer's tls directory that are referenced by orderer startup config
  cp "${PWD}/organizations/ordererOrganizations/doctor.com/orderers/orderer.doctor.com/tls/tlscacerts/"* "${PWD}/organizations/ordererOrganizations/doctor.com/orderers/orderer.doctor.com/tls/ca.crt"
  cp "${PWD}/organizations/ordererOrganizations/doctor.com/orderers/orderer.doctor.com/tls/signcerts/"* "${PWD}/organizations/ordererOrganizations/doctor.com/orderers/orderer.doctor.com/tls/server.crt"
  cp "${PWD}/organizations/ordererOrganizations/doctor.com/orderers/orderer.doctor.com/tls/keystore/"* "${PWD}/organizations/ordererOrganizations/doctor.com/orderers/orderer.doctor.com/tls/server.key"

  # Copy orderer org's CA cert to orderer's /msp/tlscacerts directory (for use in the orderer MSP definition)
  mkdir -p "${PWD}/organizations/ordererOrganizations/doctor.com/orderers/orderer.doctor.com/msp/tlscacerts"
  cp "${PWD}/organizations/ordererOrganizations/doctor.com/orderers/orderer.doctor.com/tls/tlscacerts/"* "${PWD}/organizations/ordererOrganizations/doctor.com/orderers/orderer.doctor.com/msp/tlscacerts/tlsca.doctor.com-cert.pem"

  infoln "Generating the admin msp"
  set -x
  fabric-ca-client enroll -u https://ordererAdmin:ordererAdminpw@localhost:9054 --caname ca-orderer -M "${PWD}/organizations/ordererOrganizations/doctor.com/users/Admin@doctor.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/ordererOrg/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/ordererOrganizations/doctor.com/msp/config.yaml" "${PWD}/organizations/ordererOrganizations/doctor.com/users/Admin@doctor.com/msp/config.yaml"
}

